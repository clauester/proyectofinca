    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        System.out.println(tipo);
        int idPropietario =  Integer.parseInt(txtIdPropietario.getText());
        String nombre = txtNombre.getText();
        int tamanio = Integer.parseInt(txtTamanio.getText());
        String direccion = txtDireccion.getText();
        String codigoFinca = txtCodigo.getText();

        Finca fincaData = new Finca(idPropietario,nombre,tamanio, codigoFinca ,direccion);
        
        if (tipo.equals("agregar")) {
            getdata.AddFinca(fincaData);

        } else if (tipo.equals("actualizar")) {
           getdata.UpdateFinca(fincaData, id);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

